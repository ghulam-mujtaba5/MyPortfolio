import mongoose from 'mongoose';

const ResumeSchema = new mongoose.Schema({
  filename: {
    type: String,
    required: true,
  },
  public_id: {
    type: String,
    required: true,
  },
  url: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  isActive: {
    type: Boolean,
    default: true,
  },
});

export default mongoose.models.Resume || mongoose.model('Resume', ResumeSchema);
